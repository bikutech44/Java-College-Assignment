// Increment And Decrement Operator
public class OperatorExample5 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("Pre-increment: " + (++a)); // Increment before use
        System.out.println("Post-increment: " + (a++)); // Use, then increment
        System.out.println("Value after increment: " + a);
        System.out.println("Pre-decrement: " + (--a)); // Decrement before use
        System.out.println("Post-decrement: " + (a--)); // Use, then decrement
        System.out.println("Value after decrement: " + a);
    }
}
