// Relational Operator

public class OperatorExample2 {
    public static void main(String[] args) {
        int a = 55, b = 25;

        // Equal to
        System.out.println("a == b: " + (a == b));
        // Not equal to
        System.out.println("a != b: " + (a != b));
        // Greater than
        System.out.println("a > b: " + (a > b));
        // Less than
        System.out.println("a < b: " + (a < b));
        // Greater than or equal to
        System.out.println("a >= b: " + (a >= b));
        // Less than or equal to
        System.out.println("a <= b: " + (a <= b));

    }
}
